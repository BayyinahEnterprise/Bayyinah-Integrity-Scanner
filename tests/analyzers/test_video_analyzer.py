def test_subtitle_invisible_chars_routes_through_zahir_text_analyzer() -> None:
    """Composition invariant: subtitle_invisible_chars must use the
    exact codepoint universe ZahirTextAnalyzer uses for PDF spans.

    Regression guard — if someone hardcodes a new zero-width set in
    VideoAnalyzer, the two analyzers can drift and the same byte
    sequence in a subtitle vs a PDF would classify differently. We
    assert that every codepoint the ZahirTextAnalyzer treats as
    zero-width is also what VideoAnalyzer's subtitle pipeline sees.
    """
    r = _scan_adversarial("subtitle_invisible_chars.mp4")
    finding = next(
        (f for f in r.findings if f.mechanism == "subtitle_invisible_chars"),
        None,
    )
    assert finding is not None
    # The concealed field carries the offending codepoint U+ labels
    # that ZahirTextAnalyzer's helper reported; at least one must be
    # from the shared ZERO_WIDTH_CHARS or BIDI_CONTROL_CHARS set.
    concealed = finding.concealed or ""
    zw_labels = {f"U+{ord(c):04X}" for c in ZERO_WIDTH_CHARS}
    bidi_labels = {f"U+{ord(c):04X}" for c in BIDI_CONTROL_CHARS}
    assert any(lbl in concealed for lbl in zw_labels | bidi_labels), (
        f"Expected a shared-universe codepoint label in: {concealed!r}"
    )


def test_cover_art_stego_preserves_underlying_image_evidence() -> None:
    """Composition invariant: the video_frame_stego_candidate finding
    preserves the ImageAnalyzer sub-mechanism name in its ``surface``
    so a forensics reader can trace which image-layer detector fired.
    """
    r = _scan_adversarial("cover_art_stego.mp4")
    finding = next(
        (f for f in r.findings if f.mechanism == "video_frame_stego_candidate"),
        None,
    )
    assert finding is not None
    # The surface string records the inherited mechanism name.
    assert "inherited from ImageAnalyzer" in (finding.surface or "")


