def test_scan_always_returns_integrity_report() -> None:
    # For every fixture we emit, the return must be an IntegrityReport.
    # No analyzer may leak None, list, or dict at the contract surface.
    cases = [
        FIXTURES_DIR / "clean" / "clean.mp4",
        FIXTURES_DIR / "adversarial" / "subtitle_injection.mp4",
        FIXTURES_DIR / "adversarial" / "trailing_data.mp4",
        FIXTURES_DIR / "adversarial" / "attachments.mkv",
        Path("/definitely/does/not/exist.mp4"),
    ]
    for p in cases:
        r = VideoAnalyzer().scan(p)
        assert isinstance(r, IntegrityReport)
        assert isinstance(r.integrity_score, float)
        assert 0.0 <= r.integrity_score <= 1.0
        assert isinstance(r.findings, list)
        for f in r.findings:
            # Every finding carries a registered mechanism (including
            # scan_error which lives in the batin set).
            assert f.mechanism in (ZAHIR_MECHANISMS | BATIN_MECHANISMS), (
                f"unregistered mechanism leaked: {f.mechanism}"
            )


