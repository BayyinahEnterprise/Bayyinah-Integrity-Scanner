"""
Tests for analyzers.video_analyzer.VideoAnalyzer.

Phase 24 — video container decomposition. The analyzer walks the ISO
BMFF (MP4 / MOV) box stream and the EBML (WEBM / MKV) head, then
routes each extracted stem to the analyzer that already knows how to
read that material:

  * Subtitle text → ``ZahirTextAnalyzer._check_unicode`` + regex-based
    HTML-injection shape detectors.
  * Cover-art / thumbnail image bytes → ``ImageAnalyzer().scan``.

These tests pin:

  * The BaseAnalyzer contract (inheritance, class attributes,
    supported_kinds, instantiability, return shape).
  * Each video mechanism fires on its dedicated fixture and only that
    mechanism (plus the always-on ``video_stream_inventory``).
  * The composition path actually reaches the delegated analyzer —
    ``subtitle_invisible_chars`` uses the exact codepoint set
    ZahirTextAnalyzer operates on, and ``video_frame_stego_candidate``
    fires when the cover-art image itself would fire an ImageAnalyzer
    finding.
  * Negative / edge cases (missing file, zero-byte input, extension-
    only fallback, mis-magic) return well-formed reports without
    propagating exceptions.

Fixture walker at ``tests/test_video_fixtures.py`` exercises the full
``ScanService`` dispatch path. The guardrails here stay at the
analyzer level.
"""

