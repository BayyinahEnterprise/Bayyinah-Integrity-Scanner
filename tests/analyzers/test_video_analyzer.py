def test_supported_kinds_covers_four_video_families() -> None:
    expected = frozenset({
        FileKind.VIDEO_MP4,
        FileKind.VIDEO_MOV,
        FileKind.VIDEO_WEBM,
        FileKind.VIDEO_MKV,
    })
    assert VideoAnalyzer.supported_kinds == expected


def test_supported_kinds_excludes_non_video_families() -> None:
    # Disjoint from every pre-Phase-24 analyzer's supported_kinds. A
    # regression here would mean VideoAnalyzer fires on a PDF / image /
    # DOCX and that would break the additive-only parity contract.
    for kind in (
        FileKind.PDF, FileKind.DOCX, FileKind.XLSX, FileKind.PPTX,
        FileKind.HTML, FileKind.EML, FileKind.CSV, FileKind.JSON,
        FileKind.IMAGE_PNG, FileKind.IMAGE_JPEG, FileKind.IMAGE_SVG,
        FileKind.MARKDOWN, FileKind.CODE, FileKind.UNKNOWN,
    ):
        assert kind not in VideoAnalyzer.supported_kinds, (
            f"VideoAnalyzer must not advertise support for {kind}"
        )


